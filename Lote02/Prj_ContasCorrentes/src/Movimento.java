
public class Movimento {

	private int codConta;
	private double valorMovimento;
	private double tipoMovimento; 
	private int statusMovimento;
	
	public int getCodConta() {
		return codConta;
	}
	public void setCodConta(int codConta) {
		this.codConta = codConta;
	}
	public double getValorMovimento() {
		return valorMovimento;
	}
	public void setValorMovimento(double valorMovimento) {
		this.valorMovimento = valorMovimento;
	}
	public double getTipoMovimento() {
		return tipoMovimento;
	}
	public void setTipoMovimento(double tipoMovimento) {
		this.tipoMovimento = tipoMovimento;
	}
	public int getStatusMovimento() {
		return statusMovimento;
	}
	public void setStatusMovimento(int statusMovimento) {
		this.statusMovimento = statusMovimento;
	}	
}
